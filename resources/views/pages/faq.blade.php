  <h1>FAQ</h1>

  <h3>Where are you located?</h3>
    <p>We are located in downtown Medford on the NE corner of 6th and Ivy.</p>

  <h3>What do you do?</h3>
    <p>Rogue Book Exchange is a nonprofit organization founded to unite unwanted books with people who want them. We look like a bookstore, but there's one big difference: all the books on the shelves are free!</p>

  <h3>How does it work?</h3>
    <p>A small percentage of donated books will be sold online in order to pay the bills. All other books will be made available free to the public. You don't have to donate books in order to take some home and you don't have to take any home if you donate. Someone would love to have the books and other media that have been gathering dust on your shelves or in storage so bring them on in and take home something new!</p>

  <h3>What are your hours?</h3>
    <p>Rogue Book Exchange is open Wednesday-Saturday 10am-3pm.</p>

  <h3>What do you offer?</h3>
    <ul>
      <li>Free books, movies and CDs</li>
      <li>Chairs for browsing</li>
      <li>Kid-friendly space</li>
    </ul>

  <h3>What do you carry?</h3>
    <p>Our largest selections are in general fiction, mystery/thriller/horror, religion and spirituality, nonfiction, health/diet and romance. We also have many items in young adult, reference/ educational/ textbooks/ homeschool, pregnancy/ parenting, childrens, cookbooks, biography and autobiography, science fiction/ fantasy, history and current events, business/ finance, sports, travel, pets, humor, home improvement, crafts/ hobbies, foreign language, games, computer, nature, classics, western, movies, audiobooks, and CDs. Obviously, our stock will be in constant flux so you'll have to come in to see what you can find!</p>

  <h3>What do you not accept?</h3>
    <p>Encyclopedias, Reader's Digest Condensed books or magazines.</p>

  <h3>Is there a place to drop off books after hours?</h3>
    <p>There is a donation drop box available in our parking lot for drop-off any time-- look for the book shed!</p>

  <h3>What do I do if I have a large donation?</h3>
    <p>If you have a large donation and cannot make it to us during open hours, please call to schedule an appointment.Also if you have a large number of items to donate (more than 4 full boxes) we would be happy to send a volunteer to help you box up the books or access them from storage and take them away. Contact our staff at roguebookexchange@gmail.com to arrange a pick-up time.</p>

  <h3>What kind of items do you need other then books?</h3>
    <ul>
      <li>Volunteers! We always have books ready to shelve-- the more help we have, the more books we can get out onto the shelves. We are happy to have both regularly scheduled volunteers and drop-in help.</li>
      <li>Books, movies, CDs. We need a continual flow of media to make this work!</li>
      <li>Lumber: 1x8, 1x10 and 1x12 boards in lengths of at least 3 feet.</li>
      <li>Publicity contacts. Any way we can spread the word helps.</li>
      <li>Barcode scanners.</li>
      <li>Office supplies: blank paper, packing tape, scotch tape, bubble wrap.</li>
      <li>Gift certificates (got one that you're just not sure how to use?) for building supply or office supply stores (Staples, Office Depot, Home Depot, Lowes, etc.).</li>
      <li>Bookends (the simple bent metal ones).</li>
      <li>And, of course, we'll happily accept cash donations as well.</li>
    </ul>

  <h3>Are my donations tax deductible?</h3>
    <p>Yes just ask the worker on duty for a receipt.</p>

